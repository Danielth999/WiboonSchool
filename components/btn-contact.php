<script>
document.addEventListener('DOMContentLoaded', function() {
    const toggleButton = document.getElementById('dial-toggle-button');
    const menu = document.getElementById('speed-dial-menu-default');

    if (toggleButton && menu) {
        toggleButton.addEventListener('click', function() {
            const isExpanded = toggleButton.getAttribute('aria-expanded') === 'true';

            // อัปเดตสถานะปุ่ม
            toggleButton.setAttribute('aria-expanded', String(!isExpanded));

            // ถ้าปิดอยู่ให้เปิด ถ้าเปิดอยู่ให้ปิด
            if (isExpanded) {
                // ซ่อนเมนู
                menu.classList.remove('flex');
                menu.classList.add('hidden');
                menu.classList.remove('scale-100');
                menu.classList.add('scale-0');

                // หมุนไอคอนกลับ (ถ้าต้องการ)
                toggleButton.querySelector('svg').classList.remove('rotate-45');
            } else {
                // แสดงเมนู
                menu.classList.remove('hidden');
                menu.classList.add('flex');

                // ใช้ setTimeout เพื่อให้การเปลี่ยน display ทำงานก่อน
                setTimeout(function() {
                    menu.classList.remove('scale-0');
                    menu.classList.add('scale-100');
                }, 10);

                // หมุนไอคอน (ถ้าต้องการ)
                // toggleButton.querySelector('svg').classList.add('rotate-45');
            }
        });

        // เพิ่มการปิดเมนูเมื่อคลิกที่อื่นบนหน้าเว็บ
        document.addEventListener('click', function(event) {
            if (!toggleButton.contains(event.target) && !menu.contains(event.target) &&
                menu.classList.contains('flex')) {
                // ซ่อนเมนู
                menu.classList.remove('flex');
                menu.classList.add('hidden');
                menu.classList.remove('scale-100');
                menu.classList.add('scale-0');
                toggleButton.setAttribute('aria-expanded', 'false');
            }
        });
    }
});
</script>